public class Gasket {
    int x;
    int y;
    int sub;

    public Gasket(int x, int y, int sub) {
        this.x = x;
        this.y = y;
        this.sub = sub;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getSub() {
        return sub;
    }
}
